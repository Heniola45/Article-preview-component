function showLink() {
    // document.getElementById("share_btn").className = "share-btn";
    document.getElementById("social-link").className = "social-links";
    document.getElementById("share").className = "share-change";
    document.getElementById("triangle").className = "triangle";
   

}

function hideLink(){
    document.getElementById("social-link").className = "social-links-hidden";
    document.getElementById("share").className = "share";
    document.getElementById("triangle").className = "triangle-hidden";
    

}